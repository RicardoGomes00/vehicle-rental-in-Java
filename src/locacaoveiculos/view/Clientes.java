    private void carregarTabela() {
        tableModel = new ClienteTableModel(controller.listarTodos());
        jTable1.setModel(tableModel);
    }
    
    private void permitirSomenteNumeros(JTextField campo) {
        campo.addKeyListener(new java.awt.event.KeyAdapter() {
            @Override
            public void keyTyped(java.awt.event.KeyEvent evt) {
                char c = evt.getKeyChar();

                if (!Character.isDigit(c)) {
                    evt.consume();
                }
            }
        });
    }
    
    private void adicionarListenerTabela() {
        jTable1.getSelectionModel().addListSelectionListener(new javax.swing.event.ListSelectionListener() {

            @Override
            public void valueChanged(javax.swing.event.ListSelectionEvent evt) {
                if (evt.getValueIsAdjusting()) {
                    return;
                }

                int linhaSelecionada = jTable1.getSelectedRow();

                if (linhaSelecionada == -1) {
                    limparCampos(); 
                    return;
                }

                Cliente cliente = tableModel.getClienteAt(linhaSelecionada);

                jTextField1.setText(cliente.getNome());
                jTextField2.setText(cliente.getSobrenome());
                txtRg.setText(cliente.getRg());
                txtCpf.setText(cliente.getCpf());
                jTextField3.setText(cliente.getEndereco());
            }
        });
    }
    
    private void limparCampos() {
        jTextField1.setText("");
        jTextField2.setText("");
        txtRg.setText("");
        txtCpf.setText("");
        jTextField3.setText("");

    }


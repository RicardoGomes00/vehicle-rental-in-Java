    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela.");
            return;
        }

        Cliente selecionado = tableModel.getClienteAt(row);

        selecionado.setNome(jTextField1.getText());
        selecionado.setSobrenome(jTextField2.getText());
        selecionado.setRg(txtRg.getText());
        selecionado.setCpf(txtCpf.getText());
        selecionado.setEndereco(jTextField3.getText());

        controller.atualizarCliente(selecionado);

        carregarTabela();
        limparCampos();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um cliente.");
            return;
        }

        Cliente selecionado = tableModel.getClienteAt(row);

        controller.excluirCliente(selecionado.getId());
        carregarTabela();
    }//GEN-LAST:event_jButton3ActionPerformed

